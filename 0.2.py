# Integer Data Type
age = int (input("enter your age here"))
print(age)


# Float Data Type
height = float(input("enter height"))
print(height)


# Complex Data Type
complex_num = 3 + 4j
print("Complex:", complex_num)
print("Type:", type(complex_num))


# String Data Type
name = input("enter your name here")
print(name)


# List Data Type
list_data = [1, 2, 3, 4, 5]
print("List:", list_data)
print("Type:", type(list_data))


# Tuple Data Type
tuple_data = (10, 20, 30)
print("Tuple:", tuple_data)
print("Type:", type(tuple_data))

# Range Data Type
range_data = range(5)
print("Range:", range_data)
print("As list:", list(range_data))
print("Type:", type(range_data))


# Set Data Type
set_data = {1, 2, 3, 4, 5}
print("Set:", set_data)
print("Type:", type(set_data))


# Dictionary Data Type
dict_data = {"name": "Alice", "age": 25, "city": "New York"}
print("Dictionary:", dict_data)
print("Type:", type(dict_data))


# Boolean Data Type
bool_data = True
print("Boolean:", bool_data)
print("Type:", type(bool_data))