
# data types

#variable
x,y,z=10,20,30
print(x,y,z)

# integer
x=10
print(x)
print(type(x))

#type
y=20
print(type(x))


#list
y=[1,2,3]
print(y)
print(type(y))

#string
a="ravi"
print(a)
print(type(a))

#float
x=29.5
print(x)
print(type(x))

#complex number
x=1+3j
print(x)
print(type(x))

#tuple
a=(1,2)
print(a)
print(type(a))

#range
x=range(5)
print(x)
print(type(x))

#dictionary
my_dict={'fname':'alok','lname':'saini'}
print(my_dict['fname'],my_dict['lname'])
print(type(my_dict))

#set
y={1,2,3,4}
print(y)
print(type(y))

#boolean
x=True
y=False
print(x)
print(y)
print(type(x))